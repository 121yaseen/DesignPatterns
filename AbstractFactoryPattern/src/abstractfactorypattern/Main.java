package abstractfactorypattern;

// Abstract factory pattern is very-much similar to factory method pattern
// Abstract factory pattern make use of multiple factory method patterns
// Difference is that factory method pattern constructs a single object
// and abstract factory construct multiple objects
// The abstract factory pattern provides an interface for
// creating families of related or dependent objects
// In the abstract factory there can be multiple products made
public class Main {
    public static void main(String[] args) {
        System.out.println("Abstract Factory Pattern Main");
    }


}
